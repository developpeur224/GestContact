    public function findAllOrderedByUpdateDate(bool $onlyUserContacts = false, ?User $user = null)
    {
        $qb = $this->createQueryBuilder('c')
            ->orderBy('c.miseAJourContact', 'DESC');

        if ($onlyUserContacts && $user) {
            $qb->andWhere('c.user = :user')
            ->setParameter('user', $user);
        }

        return $qb->getQuery()->getResult();
    }

